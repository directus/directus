type KeyType = 'monospace' | 'directus';

const ALPHABET = '0123456789ABCDEFGHJKMNPQRSTVWXYZ';

// random key based on this format: DXXXX-XXXXX-XXXXX-XXXXX-XXXXC
// where D is a constant
// where X is based on the alphabet
// where C is checksum of all Xes + D prefix generated by a luhnChecksum
function luhnChecksum(payload: string): string {
	let sum = 0;

	for (let i = 0; i < payload.length; i++) {
		const char = payload.at(-(1 + i)) as string;
		const value = ALPHABET.indexOf(char);
		const position = i + 1; // 1-indexed from the right of the payload

		if (position % 2 !== 0) {
			let doubled = value * 2;
			if (doubled >= 32) doubled -= 31;
			sum += doubled;
		} else {
			sum += value;
		}
	}

	const remainder = sum % 32;
	const expectedChecksumChar = ALPHABET[(32 - remainder) % 32] as string;

	return expectedChecksumChar;
}

export function generateKey(type: KeyType = 'directus', values?: string) {
	const prefix = type === 'directus' ? 'D' : 'M';

	const c =
		values && values.length === 23
			? values
			: Array.from({ length: 23 }, () => Math.floor(Math.random() * ALPHABET.length))
					.map((i) => ALPHABET[i])
					.join('');

	return `${prefix}${c.slice(0, 4)}-${c.slice(4, 9)}-${c.slice(9, 14)}-${c.slice(14, 19)}-${c.slice(19, 23) + luhnChecksum(`${prefix}${c}`)}`;
}

export function createDirectusLicense(overrides?: DeepPartial<MockDirectusLicense>): MockDirectusLicense {
	const key = overrides?.key ?? generateKey();
	const now = Math.floor(Date.now() / 1000);

	return merge(
		{
			key,
			max_projects: 10,
			projects: [],
			addons: [],
			name: `mock-${key}`,
			meta: {
				name: 'mock',
				version: '2026-05-08',
				grace_period: DAY_IN_S,
				validation_interval: 60 * 60,
				expires_at: now + 30 * DAY_IN_S,
				offline: false,
			},
			entitlements: {
				collections: { limit: -1 },
				seats: { limit: -1 },
				activity_historical_timeframe: { limit: -1 },
				revision_historical_timeframe: { limit: -1 },
				sso_enabled: { default: true },
				offline_enabled: { default: false },
				telemetry_required: { default: false },
				display_powered_by: 'HIDDEN',
				custom_llms_enabled: { default: true },
				custom_permission_rules_enabled: { default: true },
				ai_translations_enabled: { default: true },
				production_enabled: { default: true },
				flows: { limit: -1 },
			},
		},
		overrides,
	);
}

