const { privateKey, publicKey } = await generateKeyPair('EdDSA');

export { publicKey };

const TOKEN_LIFETIME_IN_S = 7 * 24 * 60 * 60;

export async function createToken(license: License) {
	const encodedToken = License.encode({ entitlements: license.entitlements, meta: license.meta });

	const now = Math.floor(Date.now() / 1000);

	return new SignJWT(encodedToken as JWTPayload)
		.setProtectedHeader({ alg: 'EdDSA' })
		.setIssuer('directus-licensing-service')
		.setAudience('directus')
		.setIssuedAt(now)
		.setJti(randomUUID())
		.setExpirationTime(now + TOKEN_LIFETIME_IN_S)
		.sign(privateKey);
}
